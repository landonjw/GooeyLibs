package ca.landonjw.gooeylibs.inventory.api;

/**
 * Represents the type of line to be drawn on a user interface in a {@link Template}.
 *
 * @author landonjw
 * @since  1.0.0
 */
public enum LineType {
    /** A vertical line, drawn from top to bottom. */
    Vertical,
    /** A horizontal line, drawn from left to right. */
    Horizontal
}
